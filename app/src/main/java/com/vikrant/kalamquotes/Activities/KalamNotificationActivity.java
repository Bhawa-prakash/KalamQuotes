    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();
        if (id == R.id.Favourite) {
            // Handle the favourite action


        } else if (id == R.id.Notification) {

            //Handle the Notification action
            Intent intent = new Intent(KalamNotificationActivity.this, NotificationActivity.class);
            startActivity(intent);

        } else if (id == R.id.ShareApp) {

            // Handle the ShareApp action
            Intent share=new Intent(Intent.ACTION_SEND);
            share.setType("text/plain");
            String text="ShareApp";
            share.putExtra(Intent.EXTRA_SUBJECT,"Services");
            share.putExtra(Intent.EXTRA_TEXT,text);
            startActivity(Intent.createChooser(share,"share via"));


        } else if (id == R.id.Contacts) {

            // Handle the Contact action
            Intent i = new Intent(Intent.ACTION_SEND);
            i.setType("message/rfc822");
            i.putExtra(Intent.EXTRA_EMAIL  , new String[]{"skapis1@outlook.com"});
            i.putExtra(Intent.EXTRA_SUBJECT, "subject of email");
            i.putExtra(Intent.EXTRA_TEXT   , "body of email");
            try {
                startActivity(Intent.createChooser(i, "Send mail..."));
            } catch (android.content.ActivityNotFoundException ex) {
                Toast.makeText(KalamNotificationActivity.this, "There are no email clients installed.", Toast.LENGTH_SHORT).show();
            }
        } else if (id == R.id.PrivacyPolicy) {

            //Handle the PrivacyPolicy action
            String url = "https://sites.google.com/view/rmitms-apps/privacy-ploicy";
            Intent i = new Intent(Intent.ACTION_VIEW);
            i.setData(Uri.parse(url));
            startActivity(i);





        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }
